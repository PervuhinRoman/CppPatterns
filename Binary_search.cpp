	int N, number; //N - –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è –∑–∞–¥–∞—é—â–∞—è –¥–ª–∏–Ω—É –º–∞—Å—Å–∏–≤–∞, number - —á–∏—Å–ª–æ –∫–æ—Ç–æ—Ä–æ–µ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ –Ω–∞–π—Ç–∏
